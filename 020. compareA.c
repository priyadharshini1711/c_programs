/*
find if a is greater or smaller than b
*/

#include <stdio.h>

int main() {
  int a = 20;
  int b = 10;
  if (a>b)
  	printf("the value of a %d is greater than value of b %d",a,b);
  else
	printf("the value of a %d is smaller than value of b %d",a,b);
  return 0;  
}

/*
output:

the value of a 20 is greater than value of b 10
*/
